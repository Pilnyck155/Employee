public class Manager extends Employee{

    public Manager(){
    }
    public Manager(int id, String name, int age, double salary, String gender, String info) {
        super(id, name, age, salary, gender, info);
    }

    String getInfo(){
        return info = " ";
    }
    @Override
    double getSalary() {
        return super.getSalary();
    }
}
